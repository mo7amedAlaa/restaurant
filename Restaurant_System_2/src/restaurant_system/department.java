    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/oRestaurant ", "root", "mohamed");
            PreparedStatement stmt = con.prepareStatement("select * from rest_dept");
            ResultSet set = stmt.executeQuery();
            DefaultTableModel dm = new DefaultTableModel();
            dm.addColumn("Dept ID");
            dm.addColumn("Dept Name");
            dm.addColumn("Dept Location");

            while (set.next()) {
                String r[] = {set.getString(1), set.getString(2), set.getString(3)};
                dm.addRow(r);
            }

            jTable1.setModel(dm);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

